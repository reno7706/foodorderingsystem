    public void loadDetails() 
    {
        Registration rA = new Registration();
        adminDetails.setRowCount(0); // Clear existing rows

        try 
        {
            List<Admin> adminList = rA.readAdmin();
            for (Admin am : adminList) 
            {
                Object[] rowData = 
                {
                    am.getAdminUserID(),
                    am.getAdminUsername(),
                    am.getEmail(),
                    am.getPhoneNumber(),
                };
                adminDetails.addRow(rowData);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
   
    
    public void displayPassword(String adminUserID) 
    {
        Registration rA = new Registration();

        try 
        {
            List<User> userList = rA.readUser();

            for (User user : userList) 
            {
                if (adminUserID.equals(user.getUserID())) 
                {
                    jTextFieldAPw.setText(user.getPassword());
                    break;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    
    
    private User getUserForAdminUserID(String adminUserID) 
    {
        Registration rA = new Registration();
        
        try 
        {
            List<User> userList = rA.readUser();

            for (User user : userList) 
            {
                if (user.getUserID().equals(adminUserID)) 
                {
                    return user;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return null;
    }
    

