    private void jButtonAEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAEActionPerformed
        int row = jTableA.getSelectedRow();

        if (row == -1) {
        // No row selected, show an error or return
            return;
        }
        
        String selectedUserID = jTextFieldAID.getText();
        String newEmail = jTextFieldAEm.getText();
        String newPhoneNumber = jTextFieldAPN.getText();
        
        Registration ea = new Registration();
        
        DefaultTableModel adminDetails = (DefaultTableModel) jTableA.getModel();
        adminDetails.setValueAt(newEmail, row, 2);
        adminDetails.setValueAt(newPhoneNumber, row, 3);
        
        try {
            ea.editAdmin(selectedUserID, newEmail, newPhoneNumber);
        }catch (IOException e) {
            e.printStackTrace(); // Handle the exception according to your application's needs
        }
        
        clearTextField();

    }//GEN-LAST:event_jButtonAEActionPerformed

    private void jButtonADActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonADActionPerformed
        int row = jTableA.getSelectedRow();
        Registration da = new Registration();
        if(row == -1)
        {
            JOptionPane.showMessageDialog(this, "Please select a row");
        }
        else
        {
            String selectedUserID = String.valueOf(adminDetails.getValueAt(row, 0));
            
            try 
            {
                da.deleteAdmin(selectedUserID);
            } catch (IOException e) {
                e.printStackTrace(); // Handle the exception according to your application's needs
            }
            
            adminDetails.removeRow(row);
            clearTextField();
        }

    }//GEN-LAST:event_jButtonADActionPerformed

