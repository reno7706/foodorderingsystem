    public void loadDetails() 
    {
        Registration rR = new Registration();
        runnerDetails.setRowCount(0); // Clear existing rows

        try 
        {
            List<Runnerprofile> runnerList = rR.readRunner();
            for (Runnerprofile rn : runnerList) 
            {
                Object[] rowData = 
                {
                    rn.getRunnerid(),
                    rn.getRunnername(),
                    rn.getGender(),
                    rn.getAge(),
                    rn.getEmail(),
                    rn.getPhonenum(),
                };
                runnerDetails.addRow(rowData);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    public void displayPassword(String runnerid) 
    {
        Registration rR = new Registration();

        try 
        {
            List<User> userList = rR.readUser();

            for (User user : userList) 
            {
                if (runnerid.equals(user.getUserID())) 
                {
                    jTextFieldRPw.setText(user.getPassword());
                    break;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    private User getUserForRunnerUserID(String runnerid) 
    {
        Registration rR = new Registration();
        
        try 
        {
            List<User> userList = rR.readUser();

            for (User user : userList) 
            {
                if (user.getUserID().equals(runnerid)) 
                {
                    return user;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return null;
    }

