    private void jButtonRCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRCActionPerformed
        String runnerid = jTextFieldRID.getText();
        String runnername = jTextFieldRUN.getText();
        String password = jTextFieldRPw.getText();
        String gender = jTextFieldRG.getText();
        String age = jTextFieldRA.getText();
        String email = jTextFieldREm.getText();
        String phonenum = jTextFieldRPN.getText();
        
        Registration rr = new Registration();
        
        Runnerprofile nR = null;
        try {
            nR = rr.registerRunner(runnerid, runnername, password, gender, age, email, phonenum);
        } catch (IOException ex) {
            Logger.getLogger(RunnerRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
        Object[] rowData = 
        {
            nR.getRunnerid(), 
            nR.getRunnername(),
            nR.getGender(),
            nR.getAge(),
            nR.getEmail(), 
            nR.getPhonenum()
        };
        runnerDetails.addRow(rowData);

        clearTextField();
    }//GEN-LAST:event_jButtonRCActionPerformed

    private void jButtonREActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonREActionPerformed
        int row = jTableR.getSelectedRow();

        if (row == -1) {
        // No row selected, show an error or return
            return;
        }
        
        String selectedUserID = jTextFieldRID.getText();
        String newGender = jTextFieldRG.getText();
        String newAge = jTextFieldRA.getText();
        String newEmail = jTextFieldREm.getText();
        String newPhonenum = jTextFieldRPN.getText();
        
        Registration er = new Registration();
        
        DefaultTableModel runnerDetails = (DefaultTableModel) jTableR.getModel();
        runnerDetails.setValueAt(newGender, row, 2);
        runnerDetails.setValueAt(newAge, row, 3);
        runnerDetails.setValueAt(newEmail, row, 4);
        runnerDetails.setValueAt(newPhonenum, row, 5);
        
        try {
            er.editRunner(selectedUserID, newGender, newAge, newEmail, newPhonenum);
        }catch (IOException e) {
            e.printStackTrace(); // Handle the exception according to your application's needs
        }
        
        clearTextField();

    }//GEN-LAST:event_jButtonREActionPerformed

    private void jButtonRDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRDActionPerformed
        int row = jTableR.getSelectedRow();
        Registration dr = new Registration();
        if(row == -1)
        {
            JOptionPane.showMessageDialog(this, "Please select a row");
        }
        else
        {
            String selectedUserID = String.valueOf(runnerDetails.getValueAt(row, 0));
            
            try 
            {
                dr.deleteRunner(selectedUserID);
            } catch (IOException e) {
                e.printStackTrace(); // Handle the exception according to your application's needs
            }
            
            runnerDetails.removeRow(row);
            clearTextField();
        }

    }//GEN-LAST:event_jButtonRDActionPerformed

